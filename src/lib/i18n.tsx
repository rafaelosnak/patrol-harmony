type Key = keyof typeof dict.pt;

interface I18nCtx { lang: Lang; t: (k: Key) => string; setLang: (l: Lang) => void; }
const I18nContext = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("pt");
  useEffect(() => {
    const stored = typeof window !== "undefined" ? (localStorage.getItem("pg.lang") as Lang | null) : null;
    if (stored === "pt" || stored === "en") setLangState(stored);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("pg.lang", l);
  };
  const t = (k: Key) => dict[lang][k] ?? k;
  return <I18nContext.Provider value={{ lang, t, setLang }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
